# Create class
class LogicalOperator:
    # Method for addition
    def add(self, num1, num2):
        # add num1, num2
        sum = num1 + num2
        return sum
    
    # Method for subtraction
    def minus(self, num1, num2):
        # subtract num1, num2
        diff = num1 - num2
        return diff
    
    # Method for multiplication
    def multiply(self, num1, num2):
        # multiply num1, num2
        product = num1 * num2
        return product
    
    # Method for division
    def divide(self, num1, num2):
        # divide num1, num2
        quotient = num1 / num2
        return quotient